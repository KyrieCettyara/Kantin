package com.java.model;

public class IPemesanan {
	private Integer yearSale;
	private Integer monthSale;
	private Integer daySale;
	private Integer totalSale;
	public Integer getYearSale() {
		return yearSale;
	}
	public void setYearSale(Integer yearSale) {
		this.yearSale = yearSale;
	}
	public Integer getMonthSale() {
		return monthSale;
	}
	public void setMonthSale(Integer monthSale) {
		this.monthSale = monthSale;
	}
	public Integer getDaySale() {
		return daySale;
	}
	public void setDaySale(Integer daySale) {
		this.daySale = daySale;
	}
	public Integer getTotalSale() {
		return totalSale;
	}
	public void setTotalSale(Integer totalSale) {
		this.totalSale = totalSale;
	}
	
	

}
